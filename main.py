import re

def is_valid_url(url: str) -> bool:
    regex = re.compile(
        r'^(https?://)?'  # http:// or https://
        r'([a-zA-Z0-9.-]+)'  # domain
        r'(\.[a-zA-Z]{2,})'  # .com, .org, etc.
        r'(/.*)?$'  # path
    )
    return re.match(regex, url) is not None

