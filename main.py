def datetime_converter(o):
    if isinstance(o, datetime):
        return o.isoformat()  # ‡πÅ‡∏õ‡∏•‡∏á datetime ‡πÄ‡∏õ‡πá‡∏ô ISO 8601 string
    elif isinstance(o, discord.Embed):
        return o.to_dict()  # ‡πÅ‡∏õ‡∏•‡∏á Embed ‡πÄ‡∏õ‡πá‡∏ô dict
    elif isinstance(o, discord.Thread):
        return {"id": o.id}  # ‡πÄ‡∏Å‡πá‡∏ö‡πÄ‡∏â‡∏û‡∏≤‡∏∞ ID ‡∏Ç‡∏≠‡∏á Thread
    elif isinstance(o, discord.Message):
        return {"id": o.id, "channel_id": o.channel.id}  # ‡πÄ‡∏Å‡πá‡∏ö‡πÄ‡∏â‡∏û‡∏≤‡∏∞ ID ‡∏Ç‡∏≠‡∏á Message ‡πÅ‡∏•‡∏∞ Channel
